// Ganjil - Genap

var counter = 1
while (counter <= 100) {
  if (counter % 2 == 0) {
    console.log(counter + " genap")
    } else {
    console.log(counter + " ganjil")  
  }
  counter = counter + 1
}
console.log(counter + " lebih dari 100")

